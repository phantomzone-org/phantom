//! # OTHERS
//!        |     4      |      4     |     4     |     4    |     4    |  5  |  5  |  5 | 
//! fence   | pred | succ |00000| rs1 | 000 |   00000  |00011| 11| Order memory operations as specified by pred and succ fields
//! simple  | imm[19:16] | imm[15:12] | imm[11:8] | imm[7:4] | imm[3:0] | rs2 | rs1 | rd | simple instruction example

pub fn sext(x: u32, i: u32) -> u32{
    x.wrapping_sub(1<<(i-1))
}

